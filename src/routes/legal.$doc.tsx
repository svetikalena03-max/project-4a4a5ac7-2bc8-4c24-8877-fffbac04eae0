export const Route = createFileRoute("/legal/$doc")({
  head: ({ params }) => {
    const doc = DOCS[(params.doc as DocKey)] ?? { title: "Документ", body: [] };
    return {
      meta: [
        { title: `${doc.title} — Баланс жизни` },
        { name: "description", content: doc.title },
      ],
    };
  },
  loader: ({ params }) => {
    if (!(params.doc in DOCS)) throw notFound();
    return null;
  },
  component: LegalPage,
  notFoundComponent: () => (
    <div className="mx-auto max-w-xl px-4 py-10 text-center text-sm text-muted-foreground">
      Документ не найден.{" "}
      <Link to="/" className="text-primary underline">На главную</Link>
    </div>
  ),
});

function LegalPage() {
  const { doc } = Route.useParams();
  const data = DOCS[doc as DocKey];

  return (
    <div className="min-h-screen w-full overflow-x-hidden bg-background px-4 py-6">
      <div className="mx-auto flex w-full max-w-xl flex-col gap-5">
        <Link
          to="/"
          className="inline-flex items-center gap-2 text-sm text-muted-foreground hover:text-foreground"
        >
          <ArrowLeft className="h-4 w-4" /> Назад
        </Link>
        <h1 className="text-2xl font-bold text-foreground">{data.title}</h1>
        <Card className="p-5">
          <div className="flex flex-col gap-3 text-sm leading-relaxed text-muted-foreground">
            {data.body.map((p, i) => (
              <p key={i}>{p}</p>
            ))}
          </div>
        </Card>
      </div>
    </div>
  );
}
